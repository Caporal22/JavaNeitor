public class Relacionales {
    public static void main(String[] args) {
        // Operadores Relacionales (ComparaciÃ³n)
        int a = 3, b = 2;
        // Igualdad ==
        var resultado = a == b;
        System.out.println("a = 3\t b = 2");
        System.out.println("Igualdad = " + resultado);
        // Distinto !=
        resultado = a == b;
        System.out.println("Distinto = " + resultado);
        // Mayor que >
        resultado = a > b;
        System.out.println("Mayor que = " + resultado);
        // Mayor o igual que <=
        resultado = a >= b;
        System.out.println("Mayor o igual que = " + resultado);
        // Menor que <
        resultado = a < b;
        System.out.println("Menor que = " + resultado);
        //Menor o igual que <=
        resultado = a <= b;
        System.out.println("Menor o igual que = " + resultado);


    }
}
